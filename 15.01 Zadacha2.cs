using System;

class Комплексное_число
{
    public double Реальная_часть { get; set; }
    public double Мнимая_часть { get; set; }

    // Конструктор для инициализации комплексного числа
    public Комплексное_число(double реальная_часть, double мнимая_часть)
    {
        Реальная_часть = реальная_часть;
        Мнимая_часть = мнимая_часть;
    }

    // Метод умножения комплексных чисел
    public Комплексное_число Умножение(Комплексное_число другое)
    {
        double новая_реальная = (Реальная_часть * другое.Реальная_часть) - (Мнимая_часть * другое.Мнимая_часть);
        double новая_мнимая = (Реальная_часть * другое.Мнимая_часть) + (Мнимая_часть * другое.Реальная_часть);

        return new Комплексное_число(новая_реальная, новая_мнимая);
    }

    // Перегруженный метод умножения для умножения комплексного числа на вещественное число
    public Комплексное_число Умножение(double вещественное_число)
    {
        double новая_реальная = Реальная_часть * вещественное_число;
        double новая_мнимая = Мнимая_часть * вещественное_число;

        return new Комплексное_число(новая_реальная, новая_мнимая);
    }

    // Метод для вывода комплексного числа в виде строки
    public override string ToString()
    {
        return $"{Реальная_часть} + {Мнимая_часть}i";
    }
}

class Primer2
{
    static void Main()
    {
        Комплексное_число комплексное1 = new Комплексное_число(2, 3);
        Комплексное_число комплексное2 = new Комплексное_число(4, 5);

        // Умножение комплексных чисел
        Комплексное_число результат1 = комплексное1.Умножение(комплексное2);
        Console.WriteLine("Умножение комплексных чисел: " + результат1);

        // Умножение комплексного числа на вещественное число
        Комплексное_число результат2 = комплексное1.Умножение(2.5);
        Console.WriteLine("Умножение комплексного числа на вещественное число: " + результат2);
    }
}
