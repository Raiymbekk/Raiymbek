using System;

class СтрокаСумматор
{
    // Метод для сложения двух строк
    public string Сумма(string строка1, string строка2)
    {
        return строка1 + строка2;
    }

    // Перегруженный метод для конкатенации строки с числом
    public string Сумма(string строка, int число)
    {
        return строка + число;
    }
}

class Primer3
{
    static void Main()
    {
        СтрокаСумматор сумматор = new СтрокаСумматор();

        // Сложение двух строк
        string результат1 = сумматор.Сумма("Привет, ", "мир!");
        Console.WriteLine("Сложение строк: " + результат1);

        // Конкатенация строки с числом
        string результат2 = сумматор.Сумма("Число: ", 42);
        Console.WriteLine("Конкатенация строки с числом: " + результат2);
    }
}
